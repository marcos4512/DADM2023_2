package com.unal.consumidor.model;

public class BornRegistry {

    private String gender;
    private String weight;
    private String size;
    private String date;
    private String momAge;
    private String dadAge;

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMomAge() {
        return momAge;
    }

    public void setMomAge(String momAge) {
        this.momAge = momAge;
    }

    public String getDadAge() {
        return dadAge;
    }

    public void setDadAge(String dadAge) {
        this.dadAge = dadAge;
    }
}
